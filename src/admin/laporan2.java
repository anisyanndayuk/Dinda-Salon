    private void tampilkanData(LocalDate start, LocalDate end) {
    modelPembelian.setRowCount(0);
    modelTransaksi.setRowCount(0);

    java.sql.Date sqlStart = java.sql.Date.valueOf(start);
    java.sql.Date sqlEnd   = java.sql.Date.valueOf(end);
    double totalBeli = 0, totalTrans = 0;

    try (Connection con = DriverManager.getConnection(
             "jdbc:mysql://localhost:3306/dinda_salon", "root", "")) {

        // === Pembelian ===
        String sqlBeli = 
          "SELECT p.id_beli, u.nama_user, p.tgl_beli, p.total_beli " +
          "FROM pembelian p JOIN user u ON p.id_user = u.id_user " +
          "WHERE p.tgl_beli BETWEEN ? AND ?";
        PreparedStatement ps1 = con.prepareStatement(sqlBeli);
        ps1.setDate(1, sqlStart);
        ps1.setDate(2, sqlEnd);
        ResultSet rs1 = ps1.executeQuery();
        while (rs1.next()) {
            modelPembelian.addRow(new Object[]{
                rs1.getString("id_beli"),
                rs1.getString("nama_user"),
                rs1.getDate("tgl_beli"),
                rs1.getDouble("total_beli")
            });
            totalBeli += rs1.getDouble("total_beli");
        }
        rs1.close();
        ps1.close();

        // === Transaksi ===
        String sqlTrans = 
          "SELECT t.id_transaksi, u.nama_user, t.tgl_transaksi, t.total_transaksi " +
          "FROM transaksi t JOIN user u ON t.id_user = u.id_user " +
          "WHERE t.tgl_transaksi BETWEEN ? AND ?";
        PreparedStatement ps2 = con.prepareStatement(sqlTrans);
        ps2.setDate(1, sqlStart);
        ps2.setDate(2, sqlEnd);
        ResultSet rs2 = ps2.executeQuery();
        while (rs2.next()) {
            modelTransaksi.addRow(new Object[]{
                rs2.getString("id_transaksi"),
                rs2.getString("nama_user"),
                rs2.getDate("tgl_transaksi"),
                rs2.getDouble("total_transaksi")
            });
            totalTrans += rs2.getDouble("total_transaksi");
        }
        rs2.close();
        ps2.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this,
            "DB error: " + e.getMessage(),
            "Error", JOptionPane.ERROR_MESSAGE);
    }

    // Update label untung/rugi
    double hasil = totalTrans - totalBeli;
    String status = hasil >= 0 ? "UNTUNG" : "RUGI";
    penjelasan.setText(String.format(
        "<html>Total Transaksi : Rp%.2f<br>Total Pembelian : Rp%.2f<br>" +
        "<b>Selisih : Rp%.2f (%s)</b></html>",
        totalTrans, totalBeli, hasil, status
    ));
}

